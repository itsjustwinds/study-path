



	bool I_had_a_wonderful_life(){
		int Negative=0;
		do{
			Coding();
			if(Sucess())--Negative;
			else Make_more_effort();
			if(!Having_a_love()) {
				Looking_for_love();
				Disappointing();
				Cry_a_lot();
				Negative++;
				continue;
			}
			Loving();
			Working_harder();
			Negative--;
		} while(Living());
		return Negative<-1e3;
	}






